#include "libft.h"

int		ft_ishexa(int c)
{
	if (ft_isdigit(c) || (c >= 'a' && c <= 'f'))
	{
		return (1);
	}
	else if (ft_isdigit(c) || (c >= 'A' && c <= 'F'))
	{
		return (1);
	}
	return (0);
}
